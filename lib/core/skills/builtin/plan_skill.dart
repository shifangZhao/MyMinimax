  category: 'planning',
  origin: 'builtin',
  version: '1.0.0',
  source: SkillSource.builtin,
  isEnabled: true,
  triggerOn: [
    // 精确场景
    '怎么实现', '如何实现', '如何做', '怎么做到',
    '帮我实现', '帮我完成', '帮我搞定',
    '实现功能', '完成功能', '开发功能',
    '解决一个问题', '解决问题', '处理这个问题',
    '请帮我', '需要实现', '需要完成',
    '做一个', '写一个', '开发一个',
    '帮我规划', '规划一下', '步骤是什么',
    '流程是什么', '应该怎么做', '要怎么做',
    'implement', 'develop', 'build', 'create', 'make',
    'how to', 'steps to', 'plan for',
  ],
  suggestedTools: [
    'readFile', 'updateFile', 'writeFile', 'createFile',
    'runCommand', 'runFlutter', 'runDart',
